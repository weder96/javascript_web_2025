// Basic declaration: `let`
// No types are declared
let numberA = 42;
let textB = "some text";
let objectC = {key : "some value"};
let arrayD = ["more text"];

// Variables can be overwritten / reassigned
numberA = 42;
// And can be assigned a different type of value
numberA = "this is actually a string"

// can also use `const` or `var` - we'll address these later
const numberE = 123;
var numberF = 99;

// Names can start with $ or _
let $first = 1
let _second = 2;